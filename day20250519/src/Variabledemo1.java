public class Variabledemo1 {
    public static void main(String[] args) {
        //byte
        byte a = 10;
        System.out.println(a);
        //short

        //int

        //long 定义long变量加L
        long b = 24954865100L;
        System.out.println(b);

        //float  定义加F
        float f =2230.12F;
        System.out.println(f);

        //double


        //char


        //boolean
        boolean c=true;
        System.out.println(c);
    }
}
